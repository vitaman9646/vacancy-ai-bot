#!/bin/bash

# ═══════════════════════════════════════════════════════════
#  VACANCY AI BOT - SETUP SCRIPT
# ═══════════════════════════════════════════════════════════

set -e  # Выход при ошибке

echo "🚀 Установка Vacancy AI Bot..."
echo ""

# ────────────────────────────────────────────────────────────
#  Проверка Python
# ────────────────────────────────────────────────────────────
if ! command -v python3 &> /dev/null; then
    echo "❌ Python 3 не найден. Установите Python 3.10+"
    exit 1
fi

PYTHON_VERSION=$(python3 --version | cut -d' ' -f2 | cut -d'.' -f1,2)
REQUIRED_VERSION="3.10"

if [ "$(printf '%s\n' "$REQUIRED_VERSION" "$PYTHON_VERSION" | sort -V | head -n1)" != "$REQUIRED_VERSION" ]; then
    echo "❌ Требуется Python 3.10+, установлено: $PYTHON_VERSION"
    exit 1
fi

echo "✅ Python $PYTHON_VERSION найден"

# ────────────────────────────────────────────────────────────
#  Создание виртуального окружения
# ────────────────────────────────────────────────────────────
echo ""
echo "📦 Создание виртуального окружения..."

if [ -d "venv" ]; then
    echo "⚠️  venv уже существует, пропускаем..."
else
    python3 -m venv venv
    echo "✅ Виртуальное окружение создано"
fi

# Активация
source venv/bin/activate

# ────────────────────────────────────────────────────────────
#  Обновление pip
# ────────────────────────────────────────────────────────────
echo ""
echo "📥 Обновление pip..."
pip install --upgrade pip setuptools wheel --quiet

# ────────────────────────────────────────────────────────────
#  Установка зависимостей
# ────────────────────────────────────────────────────────────
echo ""
echo "📚 Установка зависимостей..."
pip install -r requirements.txt

echo "✅ Зависимости установлены"

# ────────────────────────────────────────────────────────────
#  Создание директорий
# ────────────────────────────────────────────────────────────
echo ""
echo "📁 Создание необходимых директорий..."

mkdir -p logs
mkdir -p workflows
mkdir -p templates/custom
mkdir -p backup

touch workflows/.gitkeep
touch logs/.gitkeep

echo "✅ Директории созданы"

# ────────────────────────────────────────────────────────────
#  Создание .env
# ────────────────────────────────────────────────────────────
echo ""
if [ ! -f .env ]; then
    echo "⚙️  Создание .env файла..."
    cp .env.example .env
    echo ""
    echo "⚠️  ВАЖНО: Заполните .env файл вашими данными!"
    echo "   Отредактируйте: nano .env"
else
    echo "✅ .env файл уже существует"
fi

# ────────────────────────────────────────────────────────────
#  Инициализация базы данных
# ────────────────────────────────────────────────────────────
echo ""
echo "🗄️  Инициализация базы данных..."

if python scripts/setup_db.py; then
    echo "✅ База данных инициализирована"
else
    echo "⚠️  Ошибка инициализации БД (возможно, уже существует)"
fi

# ────────────────────────────────────────────────────────────
#  Проверка Chrome
# ────────────────────────────────────────────────────────────
echo ""
echo "🌐 Проверка Google Chrome..."

if command -v google-chrome &> /dev/null || command -v chromium-browser &> /dev/null; then
    echo "✅ Chrome найден"
else
    echo "⚠️  Chrome не найден!"
    echo "   Установите: sudo apt install google-chrome-stable"
fi

# ────────────────────────────────────────────────────────────
#  Финальные инструкции
# ────────────────────────────────────────────────────────────
echo ""
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo ""
echo "✅ Установка завершена!"
echo ""
echo "📋 Следующие шаги:"
echo ""
echo "1. Заполните .env файл:"
echo "   nano .env"
echo ""
echo "2. Проверьте конфигурацию:"
echo "   cat .env 
